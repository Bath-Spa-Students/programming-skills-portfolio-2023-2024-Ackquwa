places = ['himalaya', 'andes', 'tierra del fuego', 'labrador', 'guam']

print("Initial order:")
print(places)

print("\nIn alphabetical order:")
print(sorted(places))

print("\nInitial order:")
print(places)

print("\nIn reverse alphabetical order:")
print(sorted(places, reverse=True))

print("\nInitial order:")
print(places)

print("\nReversed order:")
places.reverse()
print(places)

print("\nInitial order:")
places.reverse()
print(places)

print("\nIn alphabetical order")
places.sort()
print(places)

print("\nIn reverse alphabetical order")
places.sort(reverse=True)
print(places)
